package com.my.smart;

public class Class {
    String id, Name;

    public Class(String id, String name) {
        this.id = id;
        Name = name;
    }

    public Class() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
